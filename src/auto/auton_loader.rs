pub struct AutonSetpoint {
    pub x: f64,
    pub y: f64,
    pub heading: f64,
    pub t: f64,
    pub vx: f64,
    pub vy: f64,
    pub omega: f64,
}

impl AutonSetpoint {
    fn get_drivetrain_inputs(&self) -> (Angle, Length, Angle) {
        let theta = Angle::new::<radian>(self.vy.atan2(self.vx));
        let mag = Length::new::<meter>((self.vy * self.vy + self.vx * self.vx).sqrt());
        let omega = Angle::new::<radian>(self.omega);
        (theta, mag, omega)
    }

    fn get_mirror(&mut self) -> AutonSetpoint {
        AutonSetpoint {
            x: -self.x,
            y: -self.y,
            heading: self.heading + PI,
            t: self.t,
            vx: -self.vx,
            vy: -self.vy,
            omega: -self.omega,
        }
    }
}

